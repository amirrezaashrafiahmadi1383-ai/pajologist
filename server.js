app.get('/search', (req, res) => {
    let searchTerm = req.query.field || "";
    const isPremium = false; 

    // تابع استانداردسازی حروف فارسی (ی و ک)
    const normalize = (text) => {
        if (!text) return "";
        return text
            .replace(/ی/g, "ی").replace(/ي/g, "ی")
            .replace(/ک/g, "ک").replace(/ك/g, "ک")
            .trim()
            .toLowerCase();
    };

    const term = normalize(searchTerm);

    // فیلتر کردن هوشمند: چک کردن در عنوان، نام استاد و فیلد
    let filteredProjects = pajoohanProjects.filter(p => {
        const title = normalize(p.title);
        const field = normalize(p.field);
        const prof = normalize(p.prof);
        
        return title.includes(term) || 
               field.includes(term) || 
               prof.includes(term);
    });

    if (isPremium) {
        res.json({
            status: "Premium Tier",
            count_found: filteredProjects.length,
            results: filteredProjects
        });
    } else {
        const limitedResults = filteredProjects.slice(0, 5);
        res.json({
            status: "Free Tier",
            count_found: filteredProjects.length,
            results: limitedResults,
            message: `شما در حال مشاهده ${limitedResults.length} پروژه از مجموع ${filteredProjects.length} پروژه یافت شده هستید.`
        });
    }
});

